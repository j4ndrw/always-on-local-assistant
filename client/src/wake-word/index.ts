export const hasWakeWord = (prompt: string): boolean => {
  const cleanPrompt = prompt.trim().toLowerCase().replace(/,/gi, "");
  return (
    beginningWakeWords.some(word => cleanPrompt.startsWith(word.toLowerCase())) ||
    endingWakeWords.some(word => cleanPrompt.endsWith(word.toLowerCase()))
  );
};
